static const char s_bin_to_bcd[] =
"; bin_to_bcd — convert 8-bit binary (0..255) to 3 decimal digits\n"
";\n"
"; Iterative subtraction: peel off hundreds, then tens, remainder = units.\n"
";\n"
"; PARAMS:\n"
";   INPUT    = $10   ; 8-bit binary input (preserved)\n"
";   HUNDREDS = $11   ; hundreds digit (0..2) out\n"
";   TENS     = $12   ; tens     digit (0..9) out\n"
";   UNITS    = $13   ; units    digit (0..9) out\n"
";\n"
"; Clobbers: A, X, Y, N, Z, C\n"
"; Cycles  : ~90 worst-case (value=255)\n"
";\n"
"INPUT    = $10\n"
"HUNDREDS = $11\n"
"TENS     = $12\n"
"UNITS    = $13\n"
"\n"
"bin_to_bcd:\n"
"    LDX #0\n"
"    LDY #0\n"
"    LDA INPUT\n"
".bcd_100s:\n"
"    CMP #100\n"
"    BCC .bcd_10s\n"
"    SBC #100            ; C set from CMP, so SBC = subtract without borrow\n"
"    INX\n"
"    JMP .bcd_100s\n"
".bcd_10s:\n"
"    CMP #10\n"
"    BCC .bcd_done\n"
"    SBC #10\n"
"    INY\n"
"    JMP .bcd_10s\n"
".bcd_done:\n"
"    STA UNITS\n"
"    STX HUNDREDS\n"
"    STY TENS\n"
"    RTS\n";

