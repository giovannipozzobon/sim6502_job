static const char s_mul8[] =
"; mul8 — unsigned 8x8 = 16-bit multiply (software, all 6502 variants)\n"
";\n"
"; Standard shift-and-add algorithm (8 iterations).\n"
";\n"
"; PARAMS:\n"
";   FACTOR_A  = $10   ; 8-bit multiplicand (preserved)\n"
";   FACTOR_B  = $11   ; 8-bit multiplier   (destroyed — shift register)\n"
";   RESULT_LO = $12   ; product low  byte  (out)\n"
";   RESULT_HI = $13   ; product high byte  (out)\n"
";   TMP_HI    = $14   ; high byte of shifted FACTOR_A (internal)\n"
";\n"
"; Result : [RESULT_HI:RESULT_LO] = FACTOR_A * FACTOR_B  (0..65025)\n"
"; Clobbers: A, X, FACTOR_B, TMP_HI, N, Z, C\n"
"; Cycles  : ~153 worst-case\n"
";\n"
"FACTOR_A  = $10\n"
"FACTOR_B  = $11\n"
"RESULT_LO = $12\n"
"RESULT_HI = $13\n"
"TMP_HI    = $14\n"
"\n"
"mul8:\n"
"    LDA #0\n"
"    STA RESULT_LO\n"
"    STA RESULT_HI\n"
"    STA TMP_HI\n"
"    LDX #8              ; 8 bit-iterations\n"
".mul8_loop:\n"
"    LSR FACTOR_B        ; shift multiplier right; bit 0 -> carry\n"
"    BCC .mul8_skip      ; bit was 0: skip add\n"
"    CLC\n"
"    LDA RESULT_LO\n"
"    ADC FACTOR_A\n"
"    STA RESULT_LO\n"
"    LDA RESULT_HI\n"
"    ADC TMP_HI\n"
"    STA RESULT_HI\n"
".mul8_skip:\n"
"    ASL FACTOR_A        ; shift multiplicand left (16-bit)\n"
"    ROL TMP_HI\n"
"    DEX\n"
"    BNE .mul8_loop\n"
"    RTS\n";

