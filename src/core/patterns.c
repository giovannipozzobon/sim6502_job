/* =========================================================================
 * div16 — software 16÷8 = 16-bit quotient + 8-bit remainder
 * ========================================================================= */
static const char s_div16[] =
"; div16 — unsigned 16÷8 division (software, all 6502 variants)\n"
";\n"
"; Classic restoring long-division.  The 16-bit dividend is consumed\n"
"; in-place and replaced by the 16-bit quotient on exit.\n"
";\n"
"; PARAMS:\n"
";   DIVIDEND_LO = $10   ; dividend low  byte (in); quotient low  byte (out)\n"
";   DIVIDEND_HI = $11   ; dividend high byte (in); quotient high byte (out)\n"
";   DIVISOR     = $12   ; 8-bit divisor (must be non-zero)\n"
";   REMAINDER   = $13   ; 8-bit remainder (out)\n"
";\n"
"; Clobbers: A, X, N, Z, C\n"
"; Cycles  : ~320 worst-case\n"
"; Note    : quotient is 16-bit; overflow not detected.\n"
";\n"
"DIVIDEND_LO = $10\n"
"DIVIDEND_HI = $11\n"
"DIVISOR     = $12\n"
"REMAINDER   = $13\n"
"\n"
"div16:\n"
"    LDA #0\n"
"    STA REMAINDER\n"
"    LDX #16             ; 16 bit-iterations\n"
".div16_loop:\n"
"    ASL DIVIDEND_LO     ; shift dividend/quotient left (carry <- bit 15)\n"
"    ROL DIVIDEND_HI\n"
"    ROL REMAINDER       ; shift carry into remainder\n"
"    SEC\n"
"    LDA REMAINDER\n"
"    SBC DIVISOR\n"
"    BCC .div16_no_sub   ; remainder < divisor: restore\n"
"    STA REMAINDER       ; keep subtracted remainder\n"
"    INC DIVIDEND_LO     ; set quotient bit (bit 0 was just shifted in as 0)\n"
".div16_no_sub:\n"
"    DEX\n"
"    BNE .div16_loop\n"
"    RTS\n";

