/* =========================================================================
 * delay — busy-wait delay using two nested countdown loops
 * ========================================================================= */
static const char s_delay[] =
"; delay — busy-wait delay via nested countdown loops\n"
";\n"
"; Cycle formula: (OUTER * INNER * 5) + OUTER * 9 + overhead\n"
";\n"
"; Worked example (PAL C64, ~985 kHz):\n"
";   1 frame @ 50 Hz = ~19656 cycles\n"
";   OUTER=100, INNER=39 -> 100*(39*5+9)+ovhd = 100*204 = ~20400 cycles\n"
";   Tune INNER/OUTER to taste with a cycle counter.\n"
";\n"
"; For delays longer than ~130000 cycles, call multiple times or add a\n"
"; third outer loop using a zero-page counter.\n"
";\n"
"; PARAMS:\n"
";   OUTER = $10   ; outer loop count (1..255)\n"
";   INNER = $11   ; inner loop count (1..255)\n"
";\n"
"; Clobbers: X, Y, N, Z\n"
";\n"
"OUTER = $10\n"
"INNER = $11\n"
"\n"
"delay:\n"
"    LDY OUTER           ; 3c\n"
".delay_outer:\n"
"    LDX INNER           ; 3c  per outer pass\n"
".delay_inner:\n"
"    DEX                 ; 2c\n"
"    BNE .delay_inner    ; 3c taken / 2c not taken  => ~5c per inner iter\n"
"    DEY                 ; 2c\n"
"    BNE .delay_outer    ; 3c / 2c                  => ~9c outer overhead\n"
"    RTS\n";

